#include<iostream>
#include<string>
using namespace std;

// Polymorphism: Having many forms.


int main() {
    // '+' Operator

        // Addition
    int num1 = 5;
    int num2 = 10;
    int sum = num1 + num2;
    cout << sum << endl;

        // Concatenation
    string str1 = "5";
    string str2 = "10";
    string strSum = str1 + str2;
    cout << strSum << endl;      // 510

    return 0;
}